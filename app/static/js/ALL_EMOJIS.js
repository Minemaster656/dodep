function getRandomEmoji() {
    const emojiKeys = Object.keys(EMOJIS);
    const totalEmojis = emojiKeys.reduce((sum, key) => sum + EMOJIS[key].length, 0);
    const randomIndex = Math.floor(Math.random() * totalEmojis);
    
    let currentIndex = 0;
    for (const key of emojiKeys) {
        const emojiList = EMOJIS[key];
        if (randomIndex < currentIndex + emojiList.length) {
            return emojiList[randomIndex - currentIndex];
        }
        currentIndex += emojiList.length;
    }
}




